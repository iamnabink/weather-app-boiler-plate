import 'package:flutter/material.dart';

///sizedbox constant

class SBC {

  ///sH for small Height(5)
  static SizedBox get sH => const SizedBox(height: 5);

  ///mH for medium Height(8)
  static SizedBox get mH => const SizedBox(height: 8);

  ///lH for large Height(10)
  static SizedBox get lH => const SizedBox(height: 10);

  ///xlH for extra large Height(15)
  static SizedBox get xLH => const SizedBox(height: 15);

  ///xxlH for extra large Height(20)
  static SizedBox get xxLH => const SizedBox(height: 20);

  ///sH for small Width(5)
  static SizedBox get sW => const SizedBox(width: 5);

  ///mH for medium Width(8)
  static SizedBox get mW => const SizedBox(width: 8);

  ///lH for large Width(10)
  static SizedBox get lW => const SizedBox(width: 10);

  ///xlH for extra large Width(15)
  static SizedBox get xLW => const SizedBox(width: 15);

  ///xxlH for extra large Width(20)
  static SizedBox get xXlW => const SizedBox(width: 20);
}

///SizeConstants for raw size values
class SC {
  ///small height(5)
  static const double sH = 5;

  ///medium height(10)
  static const double mH = 10;

  ///large height(15)
  static const double lH = 15;

  ///extra large height(20)
  static const double xLH = 20;

  ///small Width(5)
  static const double sW = 5;

  ///medium Width(10)
  static const double mW = 10;

  ///large Width(15)
  static const double lW = 15;

  ///extra large Width(20)
  static const double xLW = 20;
}
